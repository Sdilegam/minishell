#include "minishell.h"

int	is_p_redi(char *c)
{
	if (*c == '<' && *(c + 1) == '<')
		return (4);
	if (*c == '>' && *(c + 1) == '>')
		return (5);
	if (*c == '|')
		return (1);
	if (*c == '>')
		return (2);
	if (*c == '<')
		return (3);
	return (0);
}
